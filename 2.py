s=float(input("輸入電費度數:"))
if(s<=120):
    a=s*2.1
    nos=s*2.1
elif(121<=s<=330):
    a=120*2.1+(s-120)*3.02
    nos=120*2.1+(s-120)*2.68
elif(331<=s<=500):
    a=120*2.1+(330-120)*3.02+(s-330)*4.39
    nos=120*2.1+(330-120)*2.68+(s-330)*3.61
elif(501<=s<=700):
    a=120*2.1+(330-120)*3.02+(500-330)*4.39+(s-500)*4.97
    nos=120*2.1+(330-120)*2.68+(500-330)*3.61+(s-500)*4.01
elif(s>=701):
    a=120*2.1+(330-120)*3.02+(500-330)*4.39+(700-500)*4.97+(s-700)*5.63
    nos=120*2.1+(330-120)*2.68+(500-330)*3.61+(700-500)*4.01+(s-700)*4.5
print("Summer months:",round(a,2),"元")
print("Non-Summer months:",round(nos,2),"元")

